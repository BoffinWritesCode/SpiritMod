namespace SpiritMod.NPCs.Reach
{
    public class Gremlin : ModNPC
    {
        public override void SetStaticDefaults() {
            DisplayName.SetDefault("Gremlin");
            Main.npcFrameCount[npc.type] = Main.npcFrameCount[NPCID.BlueSlime];
        }

        public override void SetDefaults() {
            npc.width = 58;
            npc.height = 40;
            npc.damage = 36;
            npc.defense = 9;
            npc.lifeMax = 180;
            npc.HitSound = SoundID.NPCHit1;
            npc.buffImmune[BuffID.Poisoned] = true;
            npc.DeathSound = SoundID.NPCDeath1;
            npc.value = 660f;
            npc.knockBackResist = .35f;
            npc.aiStyle = 1;
            aiType = NPCID.BlueSlime;
            animationType = NPCID.BlueSlime;
        }

        public override float SpawnChance(NPCSpawnInfo spawnInfo) {
            Player player = spawnInfo.player;
            if(!(player.ZoneTowerSolar || player.ZoneTowerVortex || player.ZoneTowerNebula || player.ZoneTowerStardust) && ((!Main.pumpkinMoon && !Main.snowMoon) || spawnInfo.spawnTileY > Main.worldSurface || Main.dayTime) && (!Main.eclipse || spawnInfo.spawnTileY > Main.worldSurface || !Main.dayTime) && (SpawnCondition.GoblinArmy.Chance == 0)) {
                return spawnInfo.player.GetSpiritPlayer().ZoneReach && Main.hardMode ? 3.1f : 0f;
            }
            return 0f;
        }

        public override void HitEffect(int hitDirection, double damage) {
            if(Main.netMode != 1 && npc.life <= 0) {
                Vector2 spawnAt = npc.Center + new Vector2(0f, (float)npc.height / 2f);
                NPC.NewNPC((int)spawnAt.X, (int)spawnAt.Y, ModContent.NPCType<AngryGremlin>());
            }
        }
    }
